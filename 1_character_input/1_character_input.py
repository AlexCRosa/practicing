name = input("What is your name? ")
age = int(input("How old are you? "))

year = (2023 - age) + 100

print(f"{name} you will be 100 years old in the year of {year}.")

number = int(input("Give me a number: "))

for i in range(number):
    print(f"{name} you will be 100 years old in the year of {year}.\n")